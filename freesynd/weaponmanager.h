#include "common.h"
#include "weapon.h"
#include "ped.h"
#include <vector>

/*!
 * Weapon manager class.
 */
class WeaponManager {
  public:
    WeaponManager();
    ~WeaponManager();

    void loadWeapons();

    int numWeapons() {
        return weapons_.size();
    }

    Weapon *weapon(int n) {
        assert(n < (int) weapons_.size());
        return weapons_[n];
    }

    Weapon *findWeapon(const char *name);
    Weapon *findWeapon(Ped::WeaponIndex idx);

  protected:
    std::vector<Weapon *> weapons_;
};
